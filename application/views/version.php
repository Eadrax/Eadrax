	What's that funky number doing there?
</h2>

<p>
	WIPUP is an open-source and incrementally updated website. As new features get introduced we synchronise the production version online with the Git-hosted source at monthly intervals. We denote the last time we synched by this number in the form DD.MM.YY.
</p>

<p>
	This number is suffixed with wither an "a", a "b", or nothing at all to say whether or not the version is in alpha, beta or production quality. Alpha normally means that there's some unfinished system in place which is likely to be completely broken but it's there nevertheless, beta means there's some immature feature that needs some love before we confirm its survival, and production means ... well, that everything is perfect.
</p>

<p>
	The <a href="http://live.wipup.org/">live.WIPUP</a> version is a special version released to increase the user-developer transparency. It is an effort to show what WIPUP truly is - a work in progress: continuously changing and improving to better help the community. It is routinely updated with the latest code in the GIT repository, no matter how broken it may be. It is a useful way for people to try out the latest and greatest features. It shares the same database so you can use it just like you would with the real WIPUP.
</p>

<p>
	We want to emphasize that this is an <em>evolving</em> website - things are created based on people using it and giving feedback. The system is shaped around you, the user. The more you complain, the better it'll be.
</p>

<p>
	You can keep up to date on the "official" <a href="<?php echo url::base(); ?>projects/view/2/3/">WIPUP project</a>, which is tracked on WIPUP itself.
</p>

