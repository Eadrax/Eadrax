<h2>
	Changelog
</h2>

<p>
	Hello and welcome to WIPUP. We are a <a href="http://opendesktop.org/content/show.php/Wipup?content=110860">competition submission</a> in the Social Desktop Contest, an upcoming resource from the fine folks providing the <a href="http://e2-productions.com/repository/">Blender Model Repository</a> for the Blender community, a project headed by <a href="http://thinkmoult.com/">Dion Moult</a>, but much more importantly, <strong>a vision for a community constructed site to showcase that wonderful behind-the-scenes process that goes behind every single project</strong>.
</p>

<p>
	This website will be a rolling release work-in-progress. However we do have a <a href="http://github.com/Moult/Eadrax/tree/master">GitHub repository</a>, an <a href="http://www.assembla.com/wiki/show/eadrax/">Assembla Project</a> page as well as the <em>#eadrax</em> channel on irc.freenode.net. The list below will be the officially released list of updates available to the public (on this website itself).
</p>

<p>
	<strong>30th August</strong><br />
	Initial release of the website released online. Many things are not yet working. However the databases are all there and you are free to register an account as well as login. I would recommend not trying to use the other features yet until things are made a bit more user friendly.
</p>
