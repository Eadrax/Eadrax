<p>
	WIPUP, like the name, is a work-in-progress. It is also open source, with the code <a href="http://github.com/Moult/Eadrax">available under Git</a>. WIPUP is coded in PHP, uses a MySQL database and of course all the usual client-side languages like HTML, CSS, and Javascript (with JQuery). WIPUP uses the <a href="http://kohanaframework.org/">Kohana PHP MVC framework</a> version 2 (version 3 is HMVC). WIPUP aims to be user-centric and thus develop to please the community. <strong>We welcome developers to join us to improve WIPUP</strong>.
</p>

<p>
	Getting started is easy. Simply check out the code in the repository and set it running on your local server. If you are unfamiliar with the Kohana framework, please read the <a href="http://docs.kohanaphp.com/">Kohana v2.x docs</a>. Then have a brief peek at the WIPUP code to see where things are - the code should be well documented in-line. Work on the code on your localhost, and if things are going well, show it to us, and we'll set you up with commit access.
</p>

<p>
	Don't be afraid to ask for help or guidance. Just join the <strong>#eadrax</strong> channel on <strong>irc.freenode.net</strong> and ask. You might have to wait a while for somebody to respond, so feel free to lurk.
</p>
