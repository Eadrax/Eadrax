	Why should I use WIPUP?
</h2>

<p style="text-align: center; font-size: 20px; color: #000; text-shadow: 0px 1px 0px #CCC;">
WIPUP is a way to conveniently <strong>share</strong>, <strong>critique</strong> and <strong>track progress</strong> on your projects.
</p>

<p>
Given the mix of solutions used before such as work-in-progress forum threads, blog posts, mailing lists and  microblogging, we've created a flexible and friendly solution to answer the question "what's up?". WIPUP is not about showcasing your latest creation - rather it is about showcasing the processes behind it: the different ideas, the development, and things that didn't quite work out in the end.
</p>

<p>
The next time you're working on your latest creation, whatever it may be, take a few seconds to dump it online on WIPUP. Be it a screenshot, photograph or even just text, share it with friends, or just look at how it's changed - you'll be surprised at how much insight you may gain.
</p>

<div style="text-align: center;">
<img src="<?php echo url::base(); ?>images/tour1.png" />
</div>

<p style="text-align: center; font-size: 20px; color: #000; text-shadow: 0px 1px 0px #CCC;">
WIPUP is for people who make stuff. People who constantly have ideas bouncing around, juggle their time between various projects and start more than they finish.
</p>

<p>
Whether you just want to look back to see how far you've gone or if you want a central place for others to keep up to date, you can use WIPUP. WIPUP is designed to be flexible to match your workflow - from private usage to collaboration. As such, a WIP update can be everything from a single sentence to a full page of text, complete with formatting, images, video and downloadable files.
</p>

<div style="text-align: center;">
<img src="<?php echo url::base(); ?>images/tour2.png" />
</div>

<p>
Once an WIP update is online, anybody can add comments or kudos the update. If they like what you're up to, they can subscribe to your project or track all your activity. All of these statistics is presented to you on your dashboard along with a newsfeed to keep up to date on other projects.
</p>

<p>
Best of all, WIPUP is completely free to use - you don't even need to register an account (although it is recommended to benefit from extra features!). WIPUP is also open-source software. This means that anybody can add their own features and ideas to WIPUP, allowing it to grow and tailor itself further to the needs of  the community. So if you find something you don't quite like, we encourage user feedback and developers to join us to make WIPUP better for you.
</p>

<p style="width: 600px; margin-left: auto; margin-right: auto; text-align: center; font-size: 20px; color: #000; text-shadow: 0px 1px 0px #CCC; margin-bottom: 20px;">
It's time to stop placing  so much emphasis on the finished product and instead appreciate the  beauty of the journey behind it.
</p>

